//public表示公开的
//class表示定义一个类
//test表示一个类名
public class Test{//表示定义一个公开的类，起名test


    //类体中不允许直接编写java语句(除直接声明外)
    /*
    public表示公开的
    static表示静态的
    void表示空的
    main表示方法名是main
    (String[]   args)是一个main方法的形式参数列表

    
    以下的方法是一个程序的主方法，是程序的执行入口
    */
    public static void main(string[] args) {//表示定义一个公开的静态的主方法
        System.out.println('test!');
    }
}